elseif ($data = $editform->get_data()) {

  $interest = new object();

  $interest->datesubmitted = time();

  $interest->state = 0;

  $dataitem = $data->firstname;
  if (empty($dataitem)) $dataitem = '';
  $dataitem = trim(strip_tags($dataitem));
  $dataitem = mb_substr($dataitem, 0, 100, 'UTF-8');
  $interest->firstname = $dataitem;

  $dataitem = $data->lastname;
  if (empty($dataitem)) $dataitem = '';
  $dataitem = trim(strip_tags($dataitem));
  $dataitem = mb_substr($dataitem, 0, 100, 'UTF-8');
  $interest->lastname = $dataitem;

  $dataitem = $data->email;
  if (empty($dataitem)) $dataitem = '';
  $dataitem = trim(strip_tags($dataitem));
  $dataitem = mb_substr($dataitem, 0, 100, 'UTF-8');
  $interest->email = $dataitem;

  $dataitem = $data->country;
  if (empty($dataitem)) $dataitem = '';
  $interest->country = $dataitem;

  $dataitem = $data->course_id_1;
  if (empty($dataitem)) $dataitem = '';
  if ($dataitem === '' || (is_numeric($dataitem) && $dataitem == 0)) {
    $interest->coursename1 = '';
  }
  else {
    $interest->coursename1 = $list_for_module_select[$dataitem];
  }

  $dataitem = $data->course_id_2;
  if (empty($dataitem)) $dataitem = '';
  if ($dataitem === '' || (is_numeric($dataitem) && $dataitem == 0)) {
    $interest->coursename2 = '';
  }
  else {
    $interest->coursename2 = $list_for_module_select[$dataitem];
  }

  $dataitem = $data->suggestions;
  if (empty($dataitem)) $dataitem = '';
  $interest->suggestions = htmlspecialchars($dataitem, ENT_COMPAT, 'UTF-8');

  $interest->comment = '';

  $interest->hidden = 0;

  $DB->insert_record('peoplesinterest', $interest);


  $message  = "Expression of Interest Acknowledgement...\n\n";
  $message .= "Last Name: $interest->lastname\n\n";
  $message .= "First Name: $interest->firstname\n\n";
  $message .= "e-mail: $interest->email\n\n";
  $message .= "Country: " . $countryname[$interest->country] . "\n\n";
  $message .= "Date Submitted: " . gmdate('d/m/Y H:i', $interest->datesubmitted) . "\n\n";
  $message .= "Module 1: $interest->coursename1\n\n";
  $message .= "Module 2: $interest->coursename2\n\n";
  $message .= "Suggestions:\n" . htmlspecialchars_decode($interest->suggestions, ENT_COMPAT) . "\n";

  sendapprovedmail($interest->email, "Peoples-uni Expression of Interest in Future Courses From: $interest->lastname, $interest->firstname", $message);
  sendapprovedmail('techsupport@peoples-uni.org', "Peoples-uni Expression of Interest in Future Courses From: $interest->lastname, $interest->firstname", $message);

  redirect(new moodle_url($CFG->wwwroot . '/course/interest_form_success.php'));
}


